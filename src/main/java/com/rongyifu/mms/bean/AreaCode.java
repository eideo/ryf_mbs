package com.rongyifu.mms.bean;

/**
 * 区域编码
 *
 */
public class AreaCode {
	
	private String areaNo;
	private String areaName;
	private Integer areaLevel;
	private String attrCityNo;
	private String attrCityName;
	private String attrProvNo;
	private String attrProvName;
	
	public String getAreaNo() {
		return areaNo;
	}
	public void setAreaNo(String areaNo) {
		this.areaNo = areaNo;
	}
	public String getAreaName() {
		return areaName;
	}
	public void setAreaName(String areaName) {
		this.areaName = areaName;
	}
	public Integer getAreaLevel() {
		return areaLevel;
	}
	public void setAreaLevel(Integer areaLevel) {
		this.areaLevel = areaLevel;
	}
	public String getAttrCityNo() {
		return attrCityNo;
	}
	public void setAttrCityNo(String attrCityNo) {
		this.attrCityNo = attrCityNo;
	}
	public String getAttrCityName() {
		return attrCityName;
	}
	public void setAttrCityName(String attrCityName) {
		this.attrCityName = attrCityName;
	}
	public String getAttrProvNo() {
		return attrProvNo;
	}
	public void setAttrProvNo(String attrProvNo) {
		this.attrProvNo = attrProvNo;
	}
	public String getAttrProvName() {
		return attrProvName;
	}
	public void setAttrProvName(String attrProvName) {
		this.attrProvName = attrProvName;
	}
}
